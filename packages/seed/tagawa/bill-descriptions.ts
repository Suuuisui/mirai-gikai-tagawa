import type { Proposer } from "./source-data";

export type BillDescriptionSource =
  /** 会議録検索システムの本会議録（提案理由説明・質疑・討論等）に基づく */
  | "minutes"
  /** 議案説明資料（PDF）等、会議録以外の公開資料に基づく（会議録未公開時のフォールバック） */
  | "explanation-materials";

export interface BillDescriptionOverride {
  /** 解説文の典拠。build-csv.ts側の「## 出典」欄の文言を出し分けるために使用 */
  source: BillDescriptionSource;
  /** 一覧・OGP用の一文サマリー */
  summary: string;
  /** 解説文本文（Markdown。見出しは付けない。build-csv.ts側で `## 解説` を付与する） */
  body: string;
}

