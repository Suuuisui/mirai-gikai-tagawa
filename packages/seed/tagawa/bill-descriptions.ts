/**
 * `${sessionKey}:${billNumberLabel ?? title}:${proposer}` 形式のキーを組み立てる。
 *
 * 田川市議会では、市長提出議案と委員会提出議案・議員提出議案とで議案番号が
 * 別系統で採番されており、同一会期内で番号が重複することがある（例:
 * r5-1-teirei の「議案第9号」は市長提出の予算議案と委員会提出の条例改正
 * 議案の両方に振られている）。proposer を含めないとキーが衝突し、
 * 無関係な議案に同じ解説文が表示されてしまうため、必ず proposer まで
 * 含めたキーを使用すること。
 */
export function billDescriptionKey(
  sessionKey: string,
  billNumberLabel: string | null,
  title: string,
  proposer: Proposer
): string {
  return `${sessionKey}:${billNumberLabel ?? title}:${proposer}`;
}
